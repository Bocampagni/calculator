package application;

public class contador {
	
	public contador() {}
	
	
	public double soma(double x, double y) {
		return x + y;
	}
	
	public double sub(double x, double y) {
		return x - y;
	}
	
	public double produto(double x, double y) {
		return x * y;
	}
	
	//Quem vai tratar é a main
	public double div(int x, int y) {
		return x / y;
	}
}
